CREATE OR REPLACE FUNCTION "public"."search_usda_foods"("query_embedding" "public"."vector", "match_threshold" double precision, "match_count" integer) RETURNS TABLE("fdc_id" bigint, "name" "text", "similarity" double precision)
    LANGUAGE "sql" STABLE
    AS $$
  select
    f.fdc_id as fdc_id,
    f.name as name,
    1 - (f.embedding <=> query_embedding) as similarity
  from usda_foods f
  where f.embedding <=> query_embedding < 1 - match_threshold
  and f.data_type not ilike 'sub_sample_food'
  order by f.embedding <=> query_embedding
  limit match_count;
$$;


