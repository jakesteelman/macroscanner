CREATE OR REPLACE FUNCTION "public"."usda_hybrid_search"("query_text" "text", "query_embedding" "public"."vector", "match_count" integer, "full_text_weight" double precision DEFAULT 1, "semantic_weight" double precision DEFAULT 1, "rrf_k" integer DEFAULT 50) RETURNS SETOF "public"."usda_foods"
    LANGUAGE "sql"
    AS $$
with full_text as (
  select
    fdc_id,
    -- Note: ts_rank_cd is not indexable but will only rank matches of the where clause
    -- which shouldn't be too big
    row_number() over(order by ts_rank_cd(fts, websearch_to_tsquery(query_text)) desc) as rank_ix
  from
    usda_foods
  where
    fts @@ websearch_to_tsquery(query_text)
  order by rank_ix
  limit least(match_count, 30) * 2
),
semantic as (
  select
    fdc_id,
    row_number() over (order by embedding <#> query_embedding) as rank_ix
  from
    usda_foods
  order by rank_ix
  limit least(match_count, 30) * 2
)
select
  usda_foods.*
from
  full_text
  full outer join semantic
    on full_text.fdc_id = semantic.fdc_id
  join usda_foods
    on coalesce(full_text.fdc_id, semantic.fdc_id) = usda_foods.fdc_id
order by
  coalesce(1.0 / (rrf_k + full_text.rank_ix), 0.0) * full_text_weight +
  coalesce(1.0 / (rrf_k + semantic.rank_ix), 0.0) * semantic_weight
  desc
limit
  least(match_count, 30)
$$;


